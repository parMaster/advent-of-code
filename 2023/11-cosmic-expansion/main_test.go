package main

import (
	"image"
	"testing"

	"github.com/stretchr/testify/require"
)

func Test_Sub(t *testing.T) {
	/*
			0123456789012
		  0 ....1........
		  1 .........2...
		  2 3............
		  3 .............
		  4 .............
		  5 ........4....
		  6 .5...........
		  7 .##.........6
		  8 ..##.........
		  9 ...##........
		 10 ....##...7...
		 11 8....9.......
	*/

	require.Equal(t, 9, distance(image.Pt(1, 6), image.Pt(5, 11)))  // 5->9 = 9
	require.Equal(t, 15, distance(image.Pt(4, 0), image.Pt(9, 10))) // 1->7 = 15
	require.Equal(t, 17, distance(image.Pt(0, 2), image.Pt(7, 12))) // 3->6 = 17
	require.Equal(t, 5, distance(image.Pt(0, 11), image.Pt(5, 11))) // 8->9 = 5
}
